#creating set
A={1,2,3,4,5,6,7,8}
B={3,4,5,6,7,8,9,10}
print("The sets are:", A,B)
#intersection of sets
print("Intersection of set is: ",A & B)
#union of sets
print("Union of set is: ",A | B)
#difference of sets
print("Difference of set is: ",A - B)
